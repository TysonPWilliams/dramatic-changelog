
export function dramaticify(entry) {
    return `
    Take the following changelog entry and rewrite it as if it were narrated by a theatrical, overly dramatic fatasy bard. 
    
    Original: ${entry}
    
    Instructions:
    - Be creative, whimsical, and exaggerated
    - Use fantasy or poetic language
    - Keep it under 3 sentences
    `
}
